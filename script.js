// Запрашивать данные у пользователя необходимо с помощью prompt(), а выводить результат с помощью alert().
// Задания.
// 1. Запросите у пользователя его имя и выведите в ответ: «Привет, его имя!». 
let name = prompt('Введите ваше имя:','стесняшка');
alert('Привет, ' + name + '!');
// 2. Запросите у пользователя год его рождения, посчитайте, сколько ему лет и выведите результат. Текущий год укажите в коде как константу. 
const year = 2026;
let birth = prompt('Введите год вашего рождения:');
let age = year - birth;
alert('Вам ' + age + ' лет');
// 3. Запросите у пользователя длину стороны квадрата и выведите периметр такого квадрата. 
let side = prompt('Введите длину стороны квадрата:');
let perimeter = side * 4;
alert('Периметр квадрата: ' + perimeter);
// 4. Запросите у пользователя радиус окружности и выведите площадь такой окружности. 
let radius = prompt('Введите радиус окружности:');
let area = Math.PI * radius * radius;
alert('Площадь окружности: ' + area);
// 5. Запросите у пользователя расстояние в км между двумя городами и за сколько часов он хочет добраться. Посчитайте скорость, с которой необходимо двигаться, чтобы успеть вовремя.
let distance = prompt('Расстояние между городами (км):');
let time = prompt('За сколько часов хотите добраться:');
let speed = distance / time;
alert('Необходимая скорость: ' + speed + ' км/ч');
// 6. Реализуйте конвертор валют. Пользователь вводит доллары, программа переводит в евро. Курс валюты храните в константе. 
const exchange = 0.92; 
let dollars = prompt('Введите сумму в долларах:');
let euros = dollars * exchange;
alert(dollars + ' USD = ' + euros + ' EUR');
// 7. Пользователь указывает объем флешки в Гб. Программа должна посчитать сколько файлов размером в 820 Мб помещается на флешку. 
let gb = prompt('Объем флешки (Гб):');
let fileMB = 820;
let flashMB = gb * 1024;
let count1 = Math.floor(flashMB / fileMB);
alert('Поместится файлов: ' + count1);
// 8. Пользователь вводит сумму денег в кошельке и цену одной шоколадки. Программа выводит сколько шоколадок может купить пользователь и сколько сдачи у него останется. 
let money = prompt('Сумма денег в кошельке:');
let price = prompt('Цена одной шоколадки:');
let count = Math.floor(money / price);
let change = money % price;
alert('Можно купить: ' + count + ' шоколадок\nСдача: ' + change);
// 9. Запросите у пользователя трехзначное число и выведите его задом наперед. Для решения задачи вам понадобится оператор % (остаток от деления). 
let num = prompt('Введите трёхзначное число:');
let hundreds = Math.floor(num / 100);        
let tens = Math.floor((num % 100) / 10);     
let abab = num % 10;                       
let reversed = abab * 100 + tens * 10 + hundreds;
alert('Число задом наперёд: ' + reversed);
